  def states_by_chunk do
    chunk_by = states |> length |> div(2) |> round
    states |> Enum.chunk(chunk_by)
  end

  def stations do
    @stations
  end

  def states do
    @stations
      |> Enum.map(fn (x) -> x[:state] end)
      |> Enum.uniq
      |> Enum.sort
  end

  def stations_of_state(state) do
    stations |> Enum.filter(fn (station) -> station[:state] == state end)
  end
